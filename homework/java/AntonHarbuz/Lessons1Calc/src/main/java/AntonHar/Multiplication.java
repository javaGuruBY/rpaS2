package AntonHar;

public class Multiplication {

    private int a;
    private int b;

    public Multiplication(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public int catculate(){
        return a * b;
    }

    public int chek (int multChek){
        return a * b;
    }
}
